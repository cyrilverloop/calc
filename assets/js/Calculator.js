/**
 * @module calc
 */

/**
 * Class representing a calculator.
 */
class Calculator {

    /**
     * adds a and b.
     * @param {number} a - the number a.
     * @param {number} b - the number b.
     * @returns {number} a + b.
     */
    add(a, b) {
        return a + b;
    }
}

export default Calculator;
